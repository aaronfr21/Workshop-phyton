# installation

##Create an environment
# Windows
'''
> mkdir myproject
> cd myproject
> py -3 -m venv venv
'''

# macOS/Linux
'''
$ mkdir myproject
$ cd myproject
$ python3 -m venv venv
'''

##Activate the environment
# Windows
'''
> venv\Scripts\activate
'''

# macOS/Linux
'''
$ . venv/bin/activate
'''

##Install Flask
'''
$ pip install Flask
'''